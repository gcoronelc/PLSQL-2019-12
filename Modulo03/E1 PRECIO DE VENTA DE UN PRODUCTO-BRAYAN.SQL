CREATE OR REPLACE FUNCTION SCOTT.FN_PRECIO_VENTA
(P_COSTO_PRODUCTO NUMBER, P_GANANCIA NUMBER) 
RETURN NUMBER
IS
    PRECIO_VENTA NUMBER;
BEGIN
    
    --PROCESO
    
    PRECIO_VENTA := p_costo_producto + p_ganancia;
    
    --RETORNO
    
    RETURN PRECIO_VENTA;
    
END;
/


SELECT SCOTT.FN_PRECIO_VENTA(100,50) FROM DUAL;




